/**
 * 將 lunar-typescript 輸出的簡體文字轉為繁體
 */
export function toTraditional(text: string): string {
  if (!text) return text;

  // 先嘗試完整詞彙匹配
  if (SC_TO_TC[text]) return SC_TO_TC[text];

  // 再逐字替換
  let result = text;
  for (const [sc, tc] of Object.entries(SC_TO_TC)) {
    if (sc.length === 1 && result.includes(sc)) {
      result = result.split(sc).join(tc);
    }
  }
  return result;
}
